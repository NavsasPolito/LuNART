function output_txt = customDataTip(~,event_obj)
% Called when a line plot is clicked on, datatip is generated
%
% Written by Simone Zocca

pos = get(event_obj, 'Position');

output_txt = {...
    ['Time: ', char(timeofday(num2ruler(event_obj.Position(1),event_obj.Target.Parent.XAxis)))]...
    ['Value: ', num2str(pos(2),4)]...
    ['ID: ', event_obj.Target.DisplayName]...
};